package com.lerx.portal.obj;

public class VisitorRec {
	
	private long currDay;
	private long currWeek;
	private long currMonth;
	private long currQuarter;
	private long currYear;
	
	public long getCurrDay() {
		return currDay;
	}
	public void setCurrDay(long currDay) {
		this.currDay = currDay;
	}
	public long getCurrWeek() {
		return currWeek;
	}
	public void setCurrWeek(long currWeek) {
		this.currWeek = currWeek;
	}
	public long getCurrMonth() {
		return currMonth;
	}
	public void setCurrMonth(long currMonth) {
		this.currMonth = currMonth;
	}
	public long getCurrQuarter() {
		return currQuarter;
	}
	public void setCurrQuarter(long currQuarter) {
		this.currQuarter = currQuarter;
	}
	public long getCurrYear() {
		return currYear;
	}
	public void setCurrYear(long currYear) {
		this.currYear = currYear;
	}
	
	

}
